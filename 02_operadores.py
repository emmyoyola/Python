### Operadores comparativos###

print( 3 > 4)
print( 3 < 4)
print( 3 >= 4)
print( 3 <=  4)
print( 3 == 4)
print( 3 != 4)

print( "aa" > "bb")
print( "aa" < "bb")
print( "aa" >= "bb")
print( "aa" <= "bb")
print( "aa" == "bb")
print( "aa" != "bb")

# Operadores lógicos
print( 3 > 4 and 'Hola' > "Python")
print( 3 > 4 or 'Hola' > "Python")
# print( 3 > 4 not 'Hola' > "Python") Esto no es correcto
print(3 > 4)
print(not(3 > 4))